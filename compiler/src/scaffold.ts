/**
 * @kailash/prism-compiler — Project scaffolding
 * Generates a new Prism project with tokens, templates, and starter page.
 *
 * Usage: npx prism-scaffold --name my-app --theme enterprise
 */

import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import type { CompileTarget } from './types.js';

export interface ScaffoldOptions {
  name: string;
  theme: string;
  target: CompileTarget;
  outDir: string;
  template: string;
}

export interface ScaffoldResult {
  files: Array<{ path: string; description: string }>;
  errors: string[];
}

export function scaffold(options: ScaffoldOptions): ScaffoldResult {
  const { name, theme, target, outDir, template } = options;
  const files: ScaffoldResult['files'] = [];
  const errors: string[] = [];

  if (target === 'flutter') {
    errors.push('Flutter scaffolding not yet implemented. Use --target web.');
    return { files, errors };
  }

  const kebabName = name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');

  // package.json
  const packageJson = {
    name: kebabName,
    version: '0.1.0',
    private: true,
    type: 'module',
    scripts: {
      dev: 'vite',
      build: 'tsc -b && vite build',
      preview: 'vite preview',
    },
    dependencies: {
      '@kailash/prism-web': '^0.1.0',
      react: '^19.1.0',
      'react-dom': '^19.1.0',
    },
    devDependencies: {
      '@kailash/prism-compiler': '^0.1.0',
      '@types/react': '^19.1.0',
      '@types/react-dom': '^19.1.0',
      '@vitejs/plugin-react': '^4.5.0',
      typescript: '^5.8.0',
      vite: '^6.3.0',
    },
  };
  files.push({ path: 'package.json', description: 'Project manifest' });

  // tsconfig.json
  const tsconfig = {
    compilerOptions: {
      target: 'ES2022',
      module: 'ESNext',
      moduleResolution: 'bundler',
      lib: ['ES2022', 'DOM', 'DOM.Iterable'],
      jsx: 'react-jsx',
      strict: true,
      noUncheckedIndexedAccess: true,
      outDir: 'dist',
      rootDir: 'src',
      skipLibCheck: true,
      forceConsistentCasingInFileNames: true,
      isolatedModules: true,
      resolveJsonModule: true,
    },
    include: ['src/**/*'],
    exclude: ['node_modules', 'dist'],
  };
  files.push({ path: 'tsconfig.json', description: 'TypeScript config (strict)' });

  // vite.config.ts
  const viteConfig = `import { defineConfig } from 'vite';
import react from '@vitejs/plugin-react';

export default defineConfig({
  plugins: [react()],
});
`;
  files.push({ path: 'vite.config.ts', description: 'Vite dev server config' });

  // index.html
  const indexHtml = `<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>${name}</title>
    <link rel="stylesheet" href="/src/tokens.css" />
    <style>
      *, *::before, *::after { box-sizing: border-box; margin: 0; padding: 0; }
      body { font-family: Inter, system-ui, -apple-system, sans-serif; -webkit-font-smoothing: antialiased; }
    </style>
  </head>
  <body>
    <div id="root"></div>
    <script type="module" src="/src/main.tsx"></script>
  </body>
</html>
`;
  files.push({ path: 'index.html', description: 'HTML entry point' });

  // Theme tokens (inline enterprise tokens as default)
  const tokensCss = generateTokensCss(theme);
  files.push({ path: 'src/tokens.css', description: `${theme} theme tokens` });

  // Theme data for ThemeProvider
  const themeTs = generateThemeData(theme, kebabName);
  files.push({ path: 'src/theme.ts', description: 'Theme provider data' });

  // Main entry
  const mainTsx = generateMainEntry(name, template);
  files.push({ path: 'src/main.tsx', description: 'React entry point' });

  // Starter page
  const pageTsx = generateStarterPage(template, name);
  files.push({ path: 'src/app.tsx', description: `${template} starter page` });

  // Write all files
  mkdirSync(join(outDir, 'src'), { recursive: true });
  writeFileSync(join(outDir, 'package.json'), JSON.stringify(packageJson, null, 2) + '\n');
  writeFileSync(join(outDir, 'tsconfig.json'), JSON.stringify(tsconfig, null, 2) + '\n');
  writeFileSync(join(outDir, 'vite.config.ts'), viteConfig);
  writeFileSync(join(outDir, 'index.html'), indexHtml);
  writeFileSync(join(outDir, 'src', 'tokens.css'), tokensCss);
  writeFileSync(join(outDir, 'src', 'theme.ts'), themeTs);
  writeFileSync(join(outDir, 'src', 'main.tsx'), mainTsx);
  writeFileSync(join(outDir, 'src', 'app.tsx'), pageTsx);

  return { files, errors };
}

