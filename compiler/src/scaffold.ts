// --- Main entry point ---

function generateMainEntry(name: string, _template: string): string {
  return `import { createRoot } from 'react-dom/client';
import { ThemeProvider, LayoutProvider } from '@kailash/prism-web';
import { themeData } from './theme.js';
import { App } from './app.js';

const root = document.getElementById('root');
if (root) {
  createRoot(root).render(
    <ThemeProvider tokens={themeData} defaultMode="system">
      <LayoutProvider>
        <App />
      </LayoutProvider>
    </ThemeProvider>,
  );
}
`;
}

// --- Starter page ---

function generateStarterPage(template: string, name: string): string {
  const templateImport = `${template.charAt(0).toUpperCase() + template.slice(1)}Template`;

  switch (template) {
    case 'dashboard':
      return `import { ${templateImport}, Grid } from '@kailash/prism-web';

export function App() {
  return (
    <${templateImport}
      title="${name}"
      subtitle="Overview"
      statsRow={
        <>
          <StatCard label="Total Users" value="1,234" />
          <StatCard label="Revenue" value="$45,678" />
          <StatCard label="Orders" value="89" />
          <StatCard label="Conversion" value="3.2%" />
        </>
      }
      primaryChart={
        <div style={{ height: 300, display: 'flex', alignItems: 'center', justifyContent: 'center', border: '1px dashed var(--prism-color-border-default)', borderRadius: 8 }}>
          Chart placeholder — replace with your chart library
        </div>
      }
    />
  );
}

function StatCard({ label, value }: { label: string; value: string }) {
  return (
    <div style={{ padding: 16, borderRadius: 8, border: '1px solid var(--prism-color-border-default)', backgroundColor: 'var(--prism-color-surface-card)' }}>
      <div style={{ fontSize: 12, color: 'var(--prism-color-text-secondary)' }}>{label}</div>
      <div style={{ fontSize: 24, fontWeight: 700, color: 'var(--prism-color-text-primary)', marginTop: 4 }}>{value}</div>
    </div>
  );
}
`;

    case 'list':
      return `import { ${templateImport}, DataTable, type ColumnDef } from '@kailash/prism-web';

interface Item { id: number; name: string; status: string; [key: string]: unknown; }

const columns: ColumnDef<Item>[] = [
  { field: 'name', header: 'Name', sortable: true },
  { field: 'status', header: 'Status', sortable: true },
];

const data: Item[] = [
  { id: 1, name: 'Item One', status: 'Active' },
  { id: 2, name: 'Item Two', status: 'Draft' },
  { id: 3, name: 'Item Three', status: 'Active' },
];

export function App() {
  return (
    <${templateImport}
      title="${name}"
      content={
        <DataTable columns={columns} data={data} sorting={{ enabled: true }} pagination={{ enabled: true }} />
      }
    />
  );
}
`;

    case 'conversation':
      return `import { ${templateImport}, ChatEngine, type ChatMessage } from '@kailash/prism-web';

const messages: ChatMessage[] = [
  { id: '1', type: 'assistant', content: 'Hello! How can I help you today?', timestamp: Date.now(), sender: 'assistant' },
];

export function App() {
  return (
    <${templateImport}
      content={
        <ChatEngine
          messages={messages}
          suggestions={[
            { label: 'Help me write', value: 'help me write' },
            { label: 'Analyze data', value: 'analyze data' },
          ]}
          onSend={(msg) => console.log('Send:', msg)}
        />
      }
    />
  );
}
`;

    default:
      return `import { ${templateImport} } from '@kailash/prism-web';

export function App() {
  return (
    <${templateImport}
      title="${name}"
      content={<div style={{ padding: 24 }}>Your content here</div>}
    />
  );
}
`;
  }
}
